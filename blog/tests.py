@pytest.mark.django_db
def test_published_manager_visibility_invariant():
    pub = PostFactory(status=Post.Status.PUBLISHED)
    draft = PostFactory(status=Post.Status.DRAFT)
    sched_future = PostFactory.scheduled_future()
    sched_past = PostFactory.scheduled_past()

    visible = set(Post.objects.published())
    assert pub in visible
    assert sched_past in visible  # publish_at o'tgan -> ommaviy
    assert draft not in visible
    assert sched_future not in visible  # hali vaqti kelmagan


