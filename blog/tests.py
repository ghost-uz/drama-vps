# ---------------------------------------------------------------------------
# JSON-LD Article
# ---------------------------------------------------------------------------
@pytest.mark.django_db
def test_article_jsonld_valid(client):
    post = PostFactory(status=Post.Status.PUBLISHED, excerpt="Qisqa tavsif")
    resp = client.get(post.get_absolute_url())
    body = resp.content.decode()
    # Sahifada BIR NECHA ld+json bor (base.html WebSite + maqola grafi) — @graph'lisini topamiz
    import re

    blocks = re.findall(r'type="application/ld\+json">(.*?)</script>', body, re.S)
    graph = None
    for raw in blocks:
        parsed = json.loads(raw)
        if isinstance(parsed, dict) and "@graph" in parsed:
            graph = parsed["@graph"]
            break
    assert graph is not None, "Article @graph topilmadi"
    types = {node["@type"] for node in graph}
    assert "Article" in types
    assert "BreadcrumbList" in types
    article = next(n for n in graph if n["@type"] == "Article")
    assert article["headline"] == post.title
    assert article["description"] == "Qisqa tavsif"
    assert "datePublished" in article


